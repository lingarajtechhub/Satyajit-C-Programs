//print 1..100 using do..while

#include<stdio.h>

int main()
{
    int i;

    i = 1;//initialization
    do
    {
        printf("%d\t", i);//statement
        i++;//increment/decrement
    } while (i<=100);//condition
    

    return 0;
} // print even and odd in a one loop.
