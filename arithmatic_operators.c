//Arithmatic Operators
/*
1. Addition(+)
2. Substraction(-)
3. Multiplication (*)
4. Devision (/) => take data from Quotient
5. Modulus (%) => take data from Remainder
*/


#include<stdio.h>

int main()
{
    int a = 4, b = 2;
    printf("Addition=%d\n", (a + b));
    printf("Substraction=%d\n", (a - b));
    printf("Multiplication=%d\n", (a * b));//product
    printf("Devision=%d\n", (a / b));//Quotient
    printf("Remainder=%d\n", (a % b));//remainder
    return 0;
}
/*

2|4|2 Quotient (/)
  4
 ===
  0 => Remainder (%)

*/